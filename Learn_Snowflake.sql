-- stream task data -- automatically SQL ki Query run karta hai 

-- Source Table (STUDENT)
CREATE TABLE UNIVERSITY (
    STUDENT_ID INT,
    NAME STRING,
    DEPARTMENT STRING,
    CGPA FLOAT
);

-- Initial Data Insert

INSERT INTO UNIVERSITY VALUES
(1, 'Ali', 'CS', 3.2),
(2, 'Ahmed', 'IT', 3.5);

-- Target Table

CREATE TABLE UNIVERSITY_TARGET (
    STUDENT_ID INT,
    NAME STRING,
    DEPARTMENT STRING,
    CGPA FLOAT
);

-- Stream Create

CREATE STREAM UNIVERSITY_stream ON TABLE UNIVERSITY;

-- New Data Insert (Testing)

INSERT INTO UNIVERSITY VALUES
(3, 'Sara', 'CS', 3.8),
(4, 'Ayesha', 'SE', 3.6);

-- Ye changes stream mein capture ho jayenge
-- Task Create

CREATE TASK process_UNIVERSITY_stream
WAREHOUSE = COMPUTE_WH
SCHEDULE = '1 MINUTE'
AS
INSERT INTO UNIVERSITY_TARGET
SELECT STUDENT_ID, NAME, DEPARTMENT, CGPA
FROM UNIVERSITY_stream
WHERE METADATA$ACTION = 'INSERT';

-- Task Start

ALTER TASK process_UNIVERSITY_stream RESUME;

SELECT * FROM UNIVERSITY_TARGET;

SELECT * FROM UNIVERSITY;

SELECT *, METADATA$ACTION 
FROM UNIVERSITY_stream;

