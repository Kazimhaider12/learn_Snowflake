CREATE MATERIALIZED VIEW MV_EMPLOYEE AS
SELECT DEPARTMENT, SUM(SALARY) AS TOTAL_SALARY
FROM EMPLOYEE
WHERE SALARY BETWEEN 25000 AND 35000
GROUP BY DEPARTMENT;

SELECT * FROM MV_EMPLOYEE;

